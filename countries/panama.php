        <h4>История</h4>
        <p class="information">
            В начале XVI века различные этнические группы населяли территорию современной Аргентины: теуэльче
            (в современной Патагонии), Rehuelches, Pampas, Matacos (в районах Чако, Салта и Тукуман), Guaycures, Huerpes,
            Diaguitas, мапуче (в Неукене, Рио-Негро, Чубуте, Ла-Пампе и Буэнос-Айресе), гуарани (в Энтре Риос, Корриентес и Мисьонес) и т. д. <br>

            В 1535 году Педро де Мендоса, находясь во главе экспедиции, обладавшей большим количеством лошадей и провизии,
            основал форт Санта Мария дель Буэн Айре, столицу современной Аргентины. В 1776 году было образовано Вице-Королевство
            Рио де ла Плата, в состав которого вошли Парагвай, Аргентина, Уругвай и часть Боливии со столицей в городе Буэнос-Айресе.
            В 1805 и 1806 годах британские войска были разгромлены в их попытках завоевать Буэнос-Айрес. Крупная буржуазия возглавила
            революционное движение в 1810 году, в результате победы этого движения были образованы Объединённые Провинции Рио де ла Плата.
            25 мая 1810 года муниципальный совет объявил о роспуске Вице-Королевства и введении правления короля Фернандо VII. <br>
            В марте 1816 года в Тукумане собрались представители нескольких провинций. 9 июля была провозглашена независимость от
            Испании и создание Объединённых Провинций Южной Америки (позднее Объединённых Провинций Рио де ла Плата).
            Хотя во главе нового государства встал Верховный правитель, Конгресс не смог прийти к соглашению о форме государственной власти.
            Многие из делегатов, в частности делегаты от города и провинции Буэнос-Айрес, были сторонниками конституционной монархии.
            Указанная позиция, которая затем была изменена в пользу централизованной республиканской системы, столкнулась
            с оппозицией в лице делегатов от других провинций, которые отстаивали федеральную систему государственного устройства.
            Дискуссия между двумя фракциями привела к гражданской войне 1819 года.<br>

            Генерал Хосе де Сан-Мартин возглавил армию, которая одержала победу над испанцами. Сан-Мартин внёс
            значительный вклад в дело борьбы за независимость Аргентины, Чили и Перу.<br>

            Мир был восстановлен в 1820 году. Тем не менее, главный вопрос, а именно создание стабильной формы правления,
            оказался нерешённым. В течение следующего десятилетия Объединённые провинции переживали состояние анархии,
            которому способствовала война с Бразилией 1825—1827 годов. Бразилия потерпела поражение в конфликте по
            поводу территориальных претензий на Уругвай. В результате конфликта Уругвай стал самостоятельным государством.<br>
            В 1833 году Великобритания заняла Мальвинские острова.<br>
            Диктаторский режим Росаса был свергнут в 1852 году группой под руководством генерала Хусто Уркиса, который
            ранее являлся губернатором Энтре-Риос. Победа Уркисы была достигнута при помощи войск Уругвая и Бразилии.
            В 1853 году была принята Конституция Аргентины, а Уркиса стал первым президентом республики Аргентина.
            Провинция Буэнос-Айрес не присоединилась к конституции и в 1854 году провозгласила независимость. Взаимная
            вражда двух государств привела в 1859 году к войне. Аргентинская республика быстро одержала победу, и
            в октябре того же года провинция Буэнос-Айрес присоединилась к Конституции. Но вскоре она стала центром
            нового мятежа, направленного против федерального правительства, который разразился в 1861 году.
            Под командованием генерала Бартоломе Митре мятежники нанесли поражение в сентябре того же года национальной
            армии. 5 ноября Президент республики объявил о своей отставке. В мае следующего года Митре был избран Конгрессом
            на пост президента, а Буэнос-Айрес стал столицей Аргентины. <br>

            Следующее десятилетие ознаменовалось завоеванием Лас-Пампас, известной в настоящее время как провинция
            Рио-Негро, в ходе которого была устранена угроза со стороны аборигенов. Эта так называемая Война Пустыни
            (1879—1880) под руководством генерала Хулио Рока открыла путь к обширным территориям, благоприятным
            для развития земледелия и животноводства. В 1880 году Рока, противник возвышения Буэнос-Айреса, был избран
            на пост президента. В годы, последовавшие за его победой, Буэнос-Айрес был отделён от провинции и утверждён
            в качестве столицы Аргентины и федерального округа. В течение 50 лет, последовавших за 1880 годом,
            Аргентина добилась значительных экономических и социальных успехов. В начале XX века Аргентина превратилась
            в одну из самых богатых стран мира. Популярность Аргентины возросла с прибытием в страну миллионов европейцев...
