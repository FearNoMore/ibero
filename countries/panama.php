        В структуре земельного фонда сельскохозяйственные угодья занимают около 70 % (но преобладают пастбища). Распахана
        значительная часть территории Пампы. Благоприятное сочетание агроклиматических ресурсов определило специализацию
        страны в МРТ на зерновом хозяйстве и животноводстве на естественных пастбищах.<br>

