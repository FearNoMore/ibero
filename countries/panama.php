        <h4>Природные ресурсы</h4>
        <p class="information">
            Благодаря разнообразию рельефа и особенностям геологического строения Аргентина располагает богатой минерально-сырьевой базой
            для развития промышленности. Но залежей мирового масштаба (как в других странах Латинской Америки) здесь почти нет. 
            В западных районах имеются рудные полезные ископаемые. Страна выделяется запасами урановых, марганцевых, медных руд, 
            бериллия; есть свинцово-цинковые, вольфрамовые и железные руды. По запасам урановых руд Аргентина входит в десятку ведущих стран мира.
            <br>

            Из топливно-энергетических ресурсов наибольшее значение имеют природный газ и нефть. 
            Основные месторождения приурочены к осадочным породам в прогибах Патагонской платформы и межгорным прогибам Анд 
            (в провинциях Неукен, Мендоса, Сальта) и на Огненной Земле. Достоверные запасы природного газа Аргентины оцениваются в 
            600 миллиардов кубических метров. Добыча его возросла (особенно с открытием месторождения в провинции Неукен). 
            В Патагонии есть небольшие запасы бурых углей. <br>

            Аргентина выделяется запасами неметаллических ископаемых, в том числе серы. 
            Довольно многочисленны залежи различного строительного сырья (мрамор, гранит и др.).

            Вместе с тем геологическая изученность территории в целом низка. Но одна из основных проблем развития отраслей
            промышленности Аргентины заключается не столько в отсутствии отдельных видов сырья (хотя ощущается нехватка коксующих углей,
            бокситов, калийных солей и др.), сколько в крайне неблагоприятном их размещении (преимущественно в окраинных, малообжитых районах).
            Так, например, в Патагонии (30 % территории страны) отмечается сочетание источников минерального сырья и топлива, водных
