        бассейна река Южной Америки. Крупнейшие реки Аргентины имеют дождевой тип питания. Основной экономический
        гидроэнергетический потенциал принадлежит рекам Патагонии, берущим начало в горах, а также рекам бассейна Параны и Уругвая.
        Но используется лишь небольшая часть этого потенциала. <br>
